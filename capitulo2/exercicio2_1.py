# Converta as seguintes expressôes matemáticas para que 
# possam ser calculadas usando o interpretador Python.
# 10 + 20 x 30
# 4² / 30
# (9^4 + 2) x 6 - 1

calculo1 = 10 + 20 * 30
calculo2 = ( 4 ** 2) / 30
calculo3 = (9 ** 2 + 2) * 6 - 1

print("O resultado da primeira expressão 10 + 20 * 30 é: ", calculo1)
print(f"O resultado da segunda expressão ( 4 ** 2) / 30 é: {calculo2:.2f}") # Formatação (para que haja apenas 2 numeros pois a virgula )
print("O resultado da terceira expressão (9 ** 2 + 2) * 6 - 1 é: ", calculo3)